        public void PurchaseOrderStatusCount()
        {
            try
            {
                //-------------totalPurchaseOrderTotal----------------------//
                using (SqlConnection con = new SqlConnection(strconnect))
                {
                    con.Open();
                    SqlCommand command = new SqlCommand("SP_GetPurchaseOrderTotalCount", con);
                    command.CommandType = CommandType.StoredProcedure;
                    int totalPurchaseOrderTotal = (int)command.ExecuteScalar();

                    lblTotalPurchaseOrderCount.Text = Convert.ToString(totalPurchaseOrderTotal);
                    //TotalInvoiceCount = totalInvoiceTotal;
                }
                double TotalPurchaseOrderCount;
                TotalPurchaseOrderCount = Convert.ToDouble(lblTotalPurchaseOrderCount.Text);

                //-------------PurchaseOrderAccepted----------------------//
                using (SqlConnection con2 = new SqlConnection(strconnect))
                {
                    con2.Open();
                    SqlCommand command = new SqlCommand("SP_GetPurchaseOrderAcceptedCount", con2);
                    command.CommandType = CommandType.StoredProcedure;
                    int AcceptedCount = (int)command.ExecuteScalar();

                    lblAccepted.Text = Convert.ToString(AcceptedCount) + "/" + lblTotalPurchaseOrderCount.Text;

                    decimal TotalAccepted = Convert.ToDecimal((AcceptedCount / TotalPurchaseOrderCount) * 100);
                    decimal totalAccepted = decimal.Round(TotalAccepted, 1);
                    lblPercentAccepted.Text = Convert.ToString(totalAccepted) + "%";

                    ProgressPercentAccept.Style.Add("width", lblPercentAccepted.Text);
                }

                //-------------PurchaseOrderDeclined----------------------//

                using (SqlConnection con3 = new SqlConnection(strconnect))
                {
                    con3.Open();
