        public void GetbyAnnouncementDisplayID()
        {
            try
            {
                string ID = HttpUtility.UrlDecode(Request.QueryString["ID"]);
                lblannounceid1.Text = ID;
                using (SqlConnection UserCon = new SqlConnection(strconnect))
                {
                    SqlCommand cmd = new SqlCommand("SP_GetbyDisplayAnnouncementpageID", UserCon);
                    cmd.CommandType = System.Data.CommandType.StoredProcedure;
                    SqlDataAdapter sda = new SqlDataAdapter(cmd);
                    cmd.Parameters.AddWithValue("@id", lblannounceid1.Text);
                    DataTable dt = new DataTable();
                    sda.Fill(dt);
                    if (dt.Rows.Count > 0)
                    {
                        lblsub.Text = dt.Rows[0]["name"].ToString();
                        lbldataadded.Text = dt.Rows[0]["date_added"].ToString();
                        lblmessage.Text = dt.Rows[0]["message"].ToString();
                        string Showname = dt.Rows[0]["show_name"].ToString();

                        if(Showname =="")
                        {
                            lblPosted1.Visible = false;
                            lblnamePosted.Visible = false;
                        }
                        else
                        {
                            lblnamePosted.Visible = true;
                            lblPosted1.Visible = true;
                            lblPosted1.Text = dt.Rows[0]["Createby"].ToString();                         
                        }
                       
                        
                       
                    }
                }
            }
            catch (Exception ex)
            {
                using (SqlConnection UserCon = new SqlConnection(strconnect))
                {
