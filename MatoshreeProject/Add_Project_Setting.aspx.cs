        protected void btnSavePermission_Click(object sender, EventArgs e)
        {
            if (Page.IsValid)
            {
                try
                {
                    SqlConnection UserCon = new SqlConnection(strconnect);
                    SqlCommand UserCommand = new SqlCommand("SP_SavePermission", UserCon);
                    UserCommand.Connection = UserCon;
                    UserCommand.CommandType = CommandType.StoredProcedure;
                    UserCommand.Parameters.AddWithValue("@Permission_Name", txtPermissionName.Text);
                    UserCommand.Parameters.AddWithValue("@Permission_Description", txtDescription.Text);
                    UserCommand.Parameters.AddWithValue("@Createby", UserName); //Session value
                    UserCommand.Parameters.AddWithValue("@EmpID", UserId);
                    UserCommand.Parameters.AddWithValue("@Designation", Designation);
                    UserCon.Open();
                    dr = UserCommand.ExecuteReader();
                    while (dr.Read())
                    {
                        result = dr[0].ToString();
                    }
                    if (Result > 0)
                    {
                        Toasteralert.Visible = false;
                        deleteToaster.Visible = true;
                        lblMesDelete.Text = "Project Permission Save Successfully";
                        GridPermission.EditIndex = -1;
                        GetProjectPermissionDetails();
                    }
                    else
                    {
                        Toasteralert.Visible = false;
                        deleteToaster.Visible = true;
                        lblMesDelete.Text = "Project Permission Already Available";
                    }
                    txtPermissionName.Text = string.Empty;
                    txtDescription.Text = string.Empty;
                }
                catch (Exception ex)
                {
                    using (DeviceCon = new SqlConnection(strconnect))
                    {
                        string ErrorMessgage = ex.Message;
                        System.Diagnostics.StackTrace trace = new System.Diagnostics.StackTrace(ex, true);
